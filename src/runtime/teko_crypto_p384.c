static const TekoEccCurve P384_CURVE = {
    12, 48, P384_P, P384_N, P384_B, P384_GX, P384_GY, teko_hmac_sha384, 48
};

int teko_p384_derive_public(const uint8_t priv[48], uint8_t pub[96]) {
    return teko_ecc_derive_public(&P384_CURVE, priv, pub);
}

int teko_p384_ecdh(const uint8_t priv[48], const uint8_t peer_pub[96], uint8_t out_x[48]) {
    return teko_ecc_ecdh(&P384_CURVE, priv, peer_pub, out_x);
}

int teko_p384_ecdsa_sign(const uint8_t priv[48], const uint8_t* hash, size_t hash_len,
                         uint8_t sig[96]) {
    return teko_ecc_ecdsa_sign(&P384_CURVE, priv, hash, hash_len, sig);
}

int teko_p384_ecdsa_verify(const uint8_t pub[96], const uint8_t* hash, size_t hash_len,
                           const uint8_t sig[96]) {
    return teko_ecc_ecdsa_verify(&P384_CURVE, pub, hash, hash_len, sig);
}
