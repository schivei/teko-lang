// NIST P-256 (secp256r1) ECDH + ECDSA. Curve constants are the standard FIPS 186-4 / SEC 2
// parameters (big-endian); all logic is the curve-generic core (teko_crypto_ecc) with the
// P-256 descriptor (SHA-256 for RFC 6979 nonces).

#include "teko_crypto_p256.h"
#include "teko_crypto_ecc.h"
#include "teko_crypto_hmac.h"

// p = 2^256 - 2^224 + 2^192 + 2^96 - 1
static const uint8_t P256_P[32] = {
    0xff,0xff,0xff,0xff,0x00,0x00,0x00,0x01,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,
    0x00,0x00,0x00,0x00,0xff,0xff,0xff,0xff,0xff,0xff,0xff,0xff,0xff,0xff,0xff,0xff
};
static const uint8_t P256_N[32] = {
    0xff,0xff,0xff,0xff,0x00,0x00,0x00,0x00,0xff,0xff,0xff,0xff,0xff,0xff,0xff,0xff,
    0xbc,0xe6,0xfa,0xad,0xa7,0x17,0x9e,0x84,0xf3,0xb9,0xca,0xc2,0xfc,0x63,0x25,0x51
};
static const uint8_t P256_B[32] = {
    0x5a,0xc6,0x35,0xd8,0xaa,0x3a,0x93,0xe7,0xb3,0xeb,0xbd,0x55,0x76,0x98,0x86,0xbc,
    0x65,0x1d,0x06,0xb0,0xcc,0x53,0xb0,0xf6,0x3b,0xce,0x3c,0x3e,0x27,0xd2,0x60,0x4b
};
static const uint8_t P256_GX[32] = {
    0x6b,0x17,0xd1,0xf2,0xe1,0x2c,0x42,0x47,0xf8,0xbc,0xe6,0xe5,0x63,0xa4,0x40,0xf2,
    0x77,0x03,0x7d,0x81,0x2d,0xeb,0x33,0xa0,0xf4,0xa1,0x39,0x45,0xd8,0x98,0xc2,0x96
};
static const uint8_t P256_GY[32] = {
    0x4f,0xe3,0x42,0xe2,0xfe,0x1a,0x7f,0x9b,0x8e,0xe7,0xeb,0x4a,0x7c,0x0f,0x9e,0x16,
    0x2b,0xce,0x33,0x57,0x6b,0x31,0x5e,0xce,0xcb,0xb6,0x40,0x68,0x37,0xbf,0x51,0xf5
};

static const TekoEccCurve P256_CURVE = {
    8, 32, P256_P, P256_N, P256_B, P256_GX, P256_GY, teko_hmac_sha256, 32
};

int teko_p256_derive_public(const uint8_t priv[32], uint8_t pub[64]) {
    return teko_ecc_derive_public(&P256_CURVE, priv, pub);
}

int teko_p256_ecdh(const uint8_t priv[32], const uint8_t peer_pub[64], uint8_t out_x[32]) {
    return teko_ecc_ecdh(&P256_CURVE, priv, peer_pub, out_x);
}

int teko_p256_ecdsa_sign(const uint8_t priv[32], const uint8_t* hash, size_t hash_len,
                         uint8_t sig[64]) {
    return teko_ecc_ecdsa_sign(&P256_CURVE, priv, hash, hash_len, sig);
}

int teko_p256_ecdsa_verify(const uint8_t pub[64], const uint8_t* hash, size_t hash_len,
                           const uint8_t sig[64]) {
    return teko_ecc_ecdsa_verify(&P256_CURVE, pub, hash, hash_len, sig);
}
